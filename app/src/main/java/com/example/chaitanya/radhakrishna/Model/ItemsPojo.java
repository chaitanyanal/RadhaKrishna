package com.example.chaitanya.radhakrishna.Model;

public class ItemsPojo
{
    String id, itemName, subItemName, pricePerSubItem, quantity, total;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getItemName() {
        return itemName;
    }

    public void setItemName(String itemName) {
        this.itemName = itemName;
    }

    public String getSubItemName() {
        return subItemName;
    }

    public void setSubItemName(String subItemName) {
        this.subItemName = subItemName;
    }

    public String getPricePerSubItem() {
        return pricePerSubItem;
    }

    public void setPricePerSubItem(String pricePerSubItem) {
        this.pricePerSubItem = pricePerSubItem;
    }

    public String getQuantity() {
        return quantity;
    }

    public void setQuantity(String quantity) {
        this.quantity = quantity;
    }

    public String getTotal() {
        return total;
    }

    public void setTotal(String total) {
        this.total = total;
    }
}
